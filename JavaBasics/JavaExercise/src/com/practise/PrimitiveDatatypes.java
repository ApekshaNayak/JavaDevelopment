package com.practise;

public class PrimitiveDatatypes {

	public static void main(String[] args) {
		//The following are the eight different primitive datatypes.
		int integer = -2004320;
		short shtInterger = 20000;
		long lngInt = 677800587;
		double doubleInt = 1.245240;
		float floatNum = 2.321f ;
		boolean booleanVal = true;
		byte val= 56 ;
		char name = 'A' ;
		
		System.out.println("Integer : "+integer);
		System.out.println("ShortInteger : "+shtInterger);
		System.out.println("LongInteger : "+lngInt);
		System.out.println("DoubleInteger : "+doubleInt);
		System.out.println("Floatnumber : "+floatNum);
		System.out.println("BooleanValue : "+booleanVal);
		System.out.println("ByteValue : "+val);
		System.out.println("CharacterValue : "+name);

	}

}
